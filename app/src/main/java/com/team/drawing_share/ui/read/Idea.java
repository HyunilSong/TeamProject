package com.team.drawing_share.ui.read;

public class Idea {
    private String Image;
    private String Title;
    private String Writer;
    private String Time;

    public Idea(){}

    public String getImage() {
        return Image;
    }


    public String getTitle() {
        return Title;
    }



    public String getWriter() {
        return Writer;
    }



    public String getTime() {
        return Time;
    }


}
